let nombre = prompt("Ingrese su primer nombre");
let apellido = prompt("Ingrese su primer apellido");

let num1 = parseInt(prompt("Ingrese un primer número entero"));
let num2 = parseInt(prompt("Ingrese un segundo número entero"));

let suma = num1 + num2;
let multi = num1 * num2;
let resultado = "Bienvenido "+ nombre + " " + apellido + 
                " La suma de sus números es de: "+ suma + " y su multiplicacion es de: " + multi;
alert(resultado);




